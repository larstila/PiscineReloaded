int		ft_sqrt(int nb)
{
	int ret;
	int i;

	ret = 0;
	i = 0;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	if (nb == 9)
		return (3);
	while (i < nb / 3)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (ret);
}
